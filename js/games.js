function positionResultsBox() {
    const e = document.getElementById("gameSearch"),
        t = document.getElementById("results"),
        n = e.getBoundingClientRect();
    t.style.top = n.bottom + window.scrollY + "px", t.style.left = n.left + window.scrollX + "px", t.style.width = n.width + "px"
}

function searchGames() {
    const e = document.getElementById("gameSearch").value.toLowerCase(),
        t = document.getElementById("results");
    if (positionResultsBox(), t.innerHTML = "", !e) return void(t.style.display = "none");
    const n = gamesList.filter((t => t.title.toLowerCase().includes(e) || Array.isArray(t.Categories) && t.Categories.some((t => t.toLowerCase().includes(e))))).sort(((e, t) => e.title.localeCompare(t.title)));
    if (0 === n.length) return t.innerHTML = "<p style='padding:8px;color:#aaa;'>No results found.</p>", void(t.style.display = "block");
    n.forEach((e => {
        const n = document.createElement("div");
        n.className = "gameframe_srch", n.setAttribute("onclick", `loadgamesrch('${e.url}'); document.getElementById('results').style.display='none';`), n.innerHTML = `\n      <img onclick="localStorage.setItem('gameimage', this.src);" loading="lazy" src="${e.image}">\n      <h1 class="rotating-text">${e.title}</h1>\n    `, t.appendChild(n)
    })), t.style.display = "block"
}

function loadgamesrch(e, t) {
    document.getElementById("gameSearch").value = "", document.getElementById("results").innerHTML = "", loadgame(e, t)
}

